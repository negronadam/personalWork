package OtherPrograms;

public class zScore {
	private double x;
	private double y;
	private double zScore;
	
	public zScore(double num1, double newZScore)
	{
		this.x = num1;
		this.zScore = newZScore;
	}
	
	public double getZScore()
	{
		return zScore;
	}
	
	
	public double getX()
	{
		return x;
	}
	
	public double getY()
	{
		return y;
	}
}
