package objectOrientedProgramming;
import java.lang.Math;
public class NumbersDef {
	public void sNP2(double num1, double num2)
	{
		System.out.println("Your number plus 2 is: " + num1 + 2 + num2);
	}
	
	
	public double returnArea(double num1, double num2)
	{
		return returnArea(num1, num2);
	}
	
	public double returnRoundUp(double num1)
	{
		int roundedAnswer = (int)Math.ceil(num1);
		System.out.println("Your rounded number is: " + roundedAnswer);
		return roundedAnswer;
		
	}
}

