package objectOrientedProgramming;

public class Worker {
	private int hWorked;
	private double hRate;
	private double payCheck;

	public Worker() {
	}

	public Worker(int hWorked, double hRate) {
	}

	public int getHour(int hWorked) {
		return hWorked;
	}

	public double getRate(double hRate) {
		return hRate;
	}

	public double getPayCheck() {
		return payCheck;
	}

}
